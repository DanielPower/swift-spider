<h1>Swift Spider</h1>
<p>
	A cloud-based journalling app based on the Bullet Journal method. In very early
	development
</p>

<h2>Goals</h2>
<ul>
	<li>Cloud-based</li>
	<li>Mobile-friendly</li>
	<li>Client-side encrypted</li>
	<li>Self-hostable</li>
</ul>

<h2>Roadmap</h2>
<ul>
	<li>Account creation / authentication</li>
	<li>Client-side encryption</li>
	<li>Basic journaling functionality</li>
</ul>
